/**
 * @mainpage DLO - a basic C++-library
 * 
 * @section sec_overview Overview
 * 
 * Currently dlo divides into four parts:
 * * configuration
 * * output
 * * stringprocessing
 * * signalhandling
 * 
 * @subsection sub_sec_config Configuration
 * This module contains some highlevel abstraction of configfiles.
 * 
 * @subsection sub_sec_output Output
 * This was the first module of DLO and is still a relativly advanced 
 * one. It contains several output-functions with modern C++11-interfaces 
 * (e.g. safe variadic functions).
 * 
 * @subsection sub_sec_strpr Stringprocessing
 * This module offers some convenience-functions for working with strings.
 * 
 * As these functions are very basic and might be offered in other libraries
 * too, they are put in their own namespace.
 * 
 * @subsection sub_sec_sighndl Signalhandling
 * This module provides an easy to use C++-like interface to get
 * signalhandling done.
 * 
 * 
 */
